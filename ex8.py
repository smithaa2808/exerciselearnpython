print(formatter.format(1, 2, 3, 4)) # printing 1 2 3 4 
print(formatter.format("one", "two", "three", "four")) # printing the strings
print(formatter.format(True, False , False , True)) # printing true or false
print(formatter.format(formatter, formatter, formatter, formatter)) # printing formatter as assigned
